package com.gentler.downloaderlib.error;

/**
 * 错误处理类
 * Created by Jiantao on 2017/10/25.
 */

public class ErrorMessage {

    private String msg;
    private int code;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

}
